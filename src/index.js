const MORSE_TABLE = {
  '.-':     'a',
  '-...':   'b',
  '-.-.':   'c',
  '-..':    'd',
  '.':      'e',
  '..-.':   'f',
  '--.':    'g',
  '....':   'h',
  '..':     'i',
  '.---':   'j',
  '-.-':    'k',
  '.-..':   'l',
  '--':     'm',
  '-.':     'n',
  '---':    'o',
  '.--.':   'p',
  '--.-':   'q',
  '.-.':    'r',
  '...':    's',
  '-':      't',
  '..-':    'u',
  '...-':   'v',
  '.--':    'w',
  '-..-':   'x',
  '-.--':   'y',
  '--..':   'z',
  '.----':  '1',
  '..---':  '2',
  '...--':  '3',
  '....-':  '4',
  '.....':  '5',
  '-....':  '6',
  '--...':  '7',
  '---..':  '8',
  '----.':  '9',
  '-----':  '0',
};

const map = {
  '10': '.',
  '11': '-',
};

function decode(expr) {
  const chunks = expr.match(/.{10}/g);
  const morses = chunks
    .map((chunk) => chunk
      .match(/.{2}/g)
      .filter((pair) => pair !== '00')
      .map((pair) => map[pair] || pair)
      .join(''));
  return morses.map((morse) => MORSE_TABLE[morse] || ' ').join('');
}

module.exports = {
  decode
}
